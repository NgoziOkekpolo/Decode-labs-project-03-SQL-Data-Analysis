--Query 10: Total sales recorded
-- This query calculates the overall sales revenue by adding all values in the totalprice column
SELECT SUM(totalprice) AS totalsales
FROM ecommerce;

--Query 11: Total sales recorded by paymentmethod
-- This query calculates the total sales generated by each payment method
SELECT paymentmethod,SUM(totalprice) AS totalsales
FROM ecommerce
GROUP BY paymentmethod
ORDER BY totalsales DESC;

--Query 12: Total sales recorded by referralsource
-- This query shows how much revenue each referralsource generated
SELECT referralsource,SUM(totalprice) AS totalsales
FROM ecommerce
GROUP BY referralsource
ORDER BY totalsales DESC;

--Query 13: Total average order value
-- This query calculates the average amount spent per order
SELECT AVG(totalprice) AS averageordervalue
FROM ecommerce;

--Query 14: Average order value by product
-- This query calculates the average order value for each product
SELECT product,AVG(totalprice) AS averageordervalue
FROM ecommerce
GROUP BY product
ORDER BY averageordervalue DESC;

--Query 15: Total numbers of customer by referralsource
-- This query counts the number of orders associated with referralsource
SELECT referralsource, COUNT(*) AS totalcustomers
FROM ecommerce
GROUP BY referralsource;

--Query 16: Average quantity purchased by product
-- This query calculates the average quatity ordered for each product
SELECT product, AVG(quantity) AS averagequantity
FROM ecommerce
GROUP BY product;
